# Session 5: Letter E
import turtle

t = turtle.Turtle()
t.shape("turtle")
t.pensize(10)
t.pencolor("red")
t.penup()
t.setpos(-30, 50)
t.pendown()
t.forward(100)
t.backward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(100)
t.backward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(100)

turtle.done()
